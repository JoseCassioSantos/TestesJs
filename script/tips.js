function tip1() {
  
    let copyText = document.getElementById("tip1");
  
    
    copyText.select();
    copyText.setSelectionRange(0, 99999); 
  
  
    navigator.clipboard.writeText(copyText.value);
    
    window.alert("Copiado")
    
  }

